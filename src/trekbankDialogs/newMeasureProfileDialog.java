    private void cancelLockNumberOfMeasuresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelLockNumberOfMeasuresActionPerformed
        cancelLockNumberOfMeasures.setEnabled(false);
        cancelLockNumberOfSubMeasures.setEnabled(false);
        lockNumberOfMeasures.setEnabled(true);
        numberOfMeasures.setEnabled(true);
        finished.setEnabled(false);
        numberOfMeasures.setValue(1);
        lockNumberOfSubMeasures.setEnabled(false);
        discription.setText("");
        discription.setEnabled(false);
        lockNumberOfSubMeasures.setEnabled(false);
        numberOfSubMeasures.setValue(1);
        numberOfSubMeasures.setEnabled(false);
        currentMeasurement = 1;
        jLabel2.setText("De " + currentMeasurement + "e meting, meet");
    }//GEN-LAST:event_cancelLockNumberOfMeasuresActionPerformed

