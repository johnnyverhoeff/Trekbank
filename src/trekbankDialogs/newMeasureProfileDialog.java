    public static ArrayList<MeasureSubProfile> showDialog(java.awt.Frame parent) throws NoMeasureProfileDefined {
        newMeasureProfileDialog dialog = new newMeasureProfileDialog(parent, true);
        dialog.setVisible(true);
        
        if (finalMeasurements == null || finalMeasurements.isEmpty()){
            throw new NoMeasureProfileDefined();
        } else {
            return finalMeasurements;
        }
    }
    
