    public static boolean showDialog(java.awt.Frame parent){
        addNewProjectDialog dialog = new addNewProjectDialog(parent, true);
        dialog.setVisible(true);
        return (chosenCustomer != null && !newProjectName.isEmpty() && Database.insertNewProjectWithUniqueId(new Project(0, chosenCustomer.getId(), newProjectName)) != -1);
    }
    
