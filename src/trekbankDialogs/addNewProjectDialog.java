    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if (projectNameField.getText().isEmpty() || projectNameField.getText() == null){
            newProjectName = null;
            JOptionPane.showMessageDialog(null, "Er is geen naam ingevuld voor het nieuwe project\nVoer een naam in of annuleer", "Project toevoegen mislukt", JOptionPane.WARNING_MESSAGE);
        } else if (chosenCustomer == null){
            JOptionPane.showMessageDialog(null, "Er is geen klant gekoppeld aan dit project\nSelecteer een klant of annuleer", "Project toevoegen mislukt", JOptionPane.WARNING_MESSAGE);
        } else {
            newProjectName = projectNameField.getText();
            closeDialog(null);
        }   
        
    }//GEN-LAST:event_addButtonActionPerformed

